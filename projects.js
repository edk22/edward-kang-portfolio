/* Build a YouTube embed URL with consistent params.
   - Use youtube-nocookie.com — fewer cross-origin/cookie restrictions than youtube.com,
     and works more reliably from file:// (null) origins.
   - Autoplay requires mute=1 in modern browsers; without mute, autoplay is blocked.
   - origin param helps some browsers validate the embed for IFrame API messages. */
window.YT_EMBED = (id, { autoplay = true } = {}) => {
  const params = new URLSearchParams({
    autoplay: autoplay ? "1" : "0",
    mute:     autoplay ? "1" : "0",
    playsinline: "1",
    rel: "0",
    modestbranding: "1",
    enablejsapi: "1"
  });
  // origin only valid for http(s) — leave it off for file://
  try {
    if (location.protocol === "http:" || location.protocol === "https:") {
      params.set("origin", location.origin);
    }
  } catch (e) {}
  return `https://www.youtube-nocookie.com/embed/${id}?` + params.toString();
};
